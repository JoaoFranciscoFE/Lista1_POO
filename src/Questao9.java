public class Questao9 {
    public static void main(String[] args) {
        double numero1 = 6;
        double numero2 = 5;

        double divisao = numero1 / numero2;
        System.out.printf("A soma de %f e %f é %f\n", numero1, numero2, numero1 + numero2);
        System.out.printf("A subtração de %f e %f é %f\n", numero1, numero2, numero1 - numero2);
        System.out.printf("A multiplicação de %f e %f é %f\n", numero1, numero2, numero1 * numero2);
        System.out.printf("A divisão de %f e %f é %f ", numero1, numero2, divisao);
    }
}
